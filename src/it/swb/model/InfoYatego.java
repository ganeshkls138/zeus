package it.swb.model;

public class InfoYatego {
	
	String titoloYatego;
	String crossSelling;
	String categorie;
	
		
	public InfoYatego() {
		super();
	}


	public String getTitoloYatego() {
		return titoloYatego;
	}


	public void setTitoloYatego(String titoloYatego) {
		this.titoloYatego = titoloYatego;
	}


	public String getCrossSelling() {
		return crossSelling;
	}


	public void setCrossSelling(String crossSelling) {
		this.crossSelling = crossSelling;
	}


	public String getCategorie() {
		return categorie;
	}


	public void setCategorie(String categorie) {
		this.categorie = categorie;
	}
	
	

}
